public class Obstacle {
    int x1;
    int y1;
    int y2;
    int width;
    int height1;
    int height2;

    public Obstacle(int x1, int y1, int y2, int width, int height1, int height2) {
        this.x1 = x1;
        this.y1 = y1;
        this.y2 = y2;
        this.width = width;
        this.height1 = height1;
        this.height2 = height2;
    }
}

